                            <br>
                            <p style="text-align:justify"><b>Synopsis:</b>
                                <br> This technical workshop is designed to teach require knowledge to plan, designed and operate Linux/Unix based system for ISP/Telco/Enterprise network or campus network, or at an Internet exchange Point (IXP). The topics that will be covered are: from the very basic to the professional level knowledge and the industry related best practices.
                            </p>

                            <p style="text-align:justify"><b>Target Audience:</b>
                                <br>Entry level to senior technical and design staff of network operators or enterprise, who are involved in day-to-day network operation and planning function.
                            </p>

                            <p style="text-align:justify"><b>Pre-requisites:</b>
                                <br>It is assumed that participants have a good understanding of network operations, Internet technologies, OSI reference model, TCP/IP, operating system knowledge and relevance topic.
                            </p>

                            <b>Course Outline:</b>
                            <ul>
                                <li>Introduction to Linux</li>
                                <li>Linux Commands and the CLI, file system structure, permissions etc</li>
                                <li>Shell Scripting Basics</li>
                                <li>Apache, DNS etc</li>
                                <li>Network Monitoring & Management</li>
                                <li>SNMP, SNMP Lab Exercise etc</li>
                                <li>Observium, Nagios, Nagios Lab exercise</li>
                                <li>Smokeping, Smokeping Lab</li>
                                <li>Log Management rsyslog, Tenshi, Logstash, Rsyslog Lab</li>
                                <li>Netflow & Nfsen and Lab</li>
                                <li>Cacti & Cacti lab</li>
                                <li>Version control RANCID, SVNWeb</li>
                                <li>Netdot, Netdot Lab</li>
                                <li>Ticketing Systems (RT), Ticketing Systems (Nagios / Cacti w/RT) etc Lab</li>
                            </ul>
                            <p style="text-align:justify"><b>Other requirements:</b>
                                <br>Please be aware that participants are required to bring laptops with pre-installed Linux (preferably Ubuntu or CentOS desktop edition).
                            </p>
                            <p style="text-align:justify"><b>Maximum number of attendee:</b>
                                <br>30 participants per workshop.
                            </p>
