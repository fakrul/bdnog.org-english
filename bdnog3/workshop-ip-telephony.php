                            <h2>IP Telephony</h2><span class="label label-info">Workshop</span>
                            <br>
                            <br><span class="badge"><h5><a href="materials/BDNOG3IPTelephony.zip">(Download Slides)</a></h5></span>
                            <br>
                            <br>
                            <p style="text-align:justify"><b>Synopsis:</b>
                                <br> This technical workshop is designed for those deploying IP Telephony/VoIP services on an IP based ISP network, enterprise or campus network, or at an Internet exchange Point (IXP). The topics that will be covered are : Basic circuit switched telephony, VoIP protocols, Asterisk the open source PBX, SIP Express Router, ISP VoIP architectures.
                            </p>

                            <p style="text-align:justify"><b>Target Audience:</b>
                                <br>Entry level to senior technical and design staff of IPTSP/IGW network operators or enterprise, who are involved in day-to-day network operation and planning function.
                            </p>

                            <p style="text-align:justify"><b>Pre-requisites:</b>
                                <br>It is assumed that participants have a good understanding of network operations, Internet technologies, OSI reference model, TCP/IP, Linux operating system and relevance topic.
                            </p>

                            <p style="text-align:justify"><b>Course Outline:</b>
                                <ul>
                                    <li>An introduction to telephony past and present, and how this has evolved into Voice over IP.</li>
                                    <li>VoIP fundamentals; techniques, codecs, protocols, plus network and quality considerations.</li>
                                    <li>How to install and configure Asterisk, one of the most popular and fully feature open source PBXs available.</li>
                                    <li>Advanced techniques with Asterisk including database integration, interactive voice response (IVR) applications, billing systems, queuing / helpdesk systems, and integration with external applications.</li>
                                    <li>Configuration of the open source SIP Express Router, and a look at other open source VoIP servers.</li>
                                    <li>VoIP platform architectures and management considerations, security.</li>
                                    <li>Configuration and provisioning requirements for a variety of hardware, including Asterisk/Digium E1 cards, and SIP handsets.</li>
                                    <li>Configuring PRI/SS7 Signalling in Asterisk.</li>
                                    <li>A look at where VoIP technologies are heading, and current trends in VoIP deployments.</li>
                                </ul>
                            </p>

                            <p style="text-align:justify"><b>Other requirements:</b>
                                <br>Please be aware that participants are required to bring laptops with pre-installed Linux (preferably Ubuntu or CentOS desktop edition.), personal headphone for call testing.
                            </p>

                            <p style="text-align:justify"><b>Maximum number of attendee:</b>
                                <br>30 participants per workshop.
                            </p>
