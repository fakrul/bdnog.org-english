                            <h2>MPLS Technology & Application</h2><span class="label label-info">Workshop</span>
                            <br>
                            <br><span class="badge"><h5><a href="materials/bdNOG2_MPLS.zip">(Download Slides)</a></h5></span>
                            <br>
                            <br>
                            <p style="text-align:justify"><b>Synopsis:</b>
                                <br> This technical workshop is made up of lectures and hands-on lab exercise to teach both basic and advance concept of MPLS, MPLS VPN both L3 & L2, Traffic Engineering Techniques, MPLS network design principles, LDP, BGP as signalling protocol etc. Also presented for understanding and to encourage best industry practice.
                            </p>
                            <p style="text-align:justify">The course includes considerable practical work based on IPv4 as well as IPv6 and router configuration commands.
                            </p>

                            <p style="text-align:justify"><b>Target Audience:</b>
                                <br>Mid or high label technical staff of and ISP or enterprise who are involve in day-to-day network operation function.
                            </p>

                            <p style="text-align:justify"><b>Pre-requisites:</b>
                                <br>It is assumed that participants have a clear understanding of network operations, Internet technologies, OSI reference model, TCP/IP, OSPF/IS-IS. They must be fluent in BGP, and/or encouraged to complete one of APNIC/APRICOT/SANOG/bdNOG BGP Workshops, BGP routing protocol and router operating software (i.e Cisco IOS etc).
                            </p>

                            <p style="text-align:justify"><b>Course Outline:</b>
                                <ul>
                                    <li>Introduction to MPLS</li>
                                    <li>MPLS Label Distribution Protocol</li>
                                    <li>MPLS LDP Configuration (Lab Exercise)</li>
                                    <li>MPLS Layer 3 VPN Concept & Architecture</li>
                                    <li>MPLS L3 VPN Configuration (Lab Exercise)</li>
                                    <li>MPLS Layer 2 VPN Concept & Architecture</li>
                                    <li>MPLS L2 VPN Configuration (Lab Exercise)</li>
                                    <li>MPLS Traffic Engineering</li>
                                </ul>
                            </p>

                            <b>Lab Exercise Module (With 20 Routers Lab):</b>
                            <ul>
                                <li>Module 1: LDP Configuration and Analysis</li>
                                <li>Module 2: MPLS L3 VPN Configuration
                                    <ul>
                                        <li>Site-to-site VPN</li>
                                        <li>Hub and Spoke VPN</li>
                                        <li>Shared Extranet VPN (Import-RT Map/Export-RT Map tools)</li>
                                        <li>6vPE & 6PE</li>
                                    </ul>
                                </li>
                                <li>Module 3: MPLS L2 VPN Configuration
                                    <ul>
                                        <li>Point-to-point (Pseudowire)</li>
                                        <li>Virtual Private LAN Service (VPLS) Config Analysis</li>
                                    </ul>
                                </li>
                                <li>Module 4: Traffic Engineering Tunnel to manipulate IGP path</li>
                            </ul>
                            <p style="text-align:justify"><b>Maximum number of attendee:</b>
                                <br>30 participants per workshop.
