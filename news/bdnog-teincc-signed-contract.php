                                <p style="text-align:justify">bdNOG has signed a contract with Trans-Eurasia Information Network Co-operation Center <a href="http://www.teincc.org/" target="_blank">(TEIN**CC)</a> to conduct technical workshop during bdNOG2 Conference. Objective of this workshop is to train both local and TEIN member countries network engineers on the Internet operational technologies.</p>

                                <p style="text-align:justify"><a href="http://www.tein.asia/" target="_blank">TEIN (Trans-Eurasia Information Network)</a> is a high speed network for research and education which connects 20 countries in Asia and 34 countries in Europe. This network is actively being used for international joint researches in cutting-edge areas, including information telecommunications and bioengineering. TEIN*CC is established to manage the 4th phase of the TEIN project and the latter was adopted as one of the ASEM projects.</p>

                                <p style="text-align:justify">The early phases of the TEIN projects were managed by DANTE, a not-for-profit organization based in the United Kingdom. In October 2010, at the ASEM8 Summit that was held in Brussels, Belgium, Korea was granted the operation right to manage the 4th phase of the TEIN project. The project was launched in 2012, with financial supports from the <a href="http://ec.europa.eu/index_en.htm" target="_blank">European Commission (EC)</a> as well as members of the participating countries.</p>

